function pendulum(values) {
    const evens = [];
    const odds = [];
    const sortedValues = values.sort((a, b) => b - a).reverse();

    for (let i = 0; i < sortedValues.length; i++) {
        if (i % 2 === 0) {
            evens.push(sortedValues[i]);
        } else {
            odds.push(sortedValues[i]);
        }
    }

    return evens.reverse().concat(odds);
};

