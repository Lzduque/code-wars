function accum(s) {
    const letters = s.toLowerCase().split('');
    const acc = [];
    for (let i = 0; i < letters.length; i++) {
        acc.push(letters[i].toUpperCase() + letters[i].repeat(i));
    }
    return acc.join('-');
}

